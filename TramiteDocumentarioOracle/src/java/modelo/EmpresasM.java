package modelo;

public class EmpresasM {
    private String CodEmpre, NomEmpre, RucEmpre,
            DirEmpre, TelEmpre, CodUbi;

    public String getCodEmpre() {
        return CodEmpre;
    }

    public void setCodEmpre(String CodEmpre) {
        this.CodEmpre = CodEmpre;
    }

    public String getNomEmpre() {
        return NomEmpre;
    }

    public void setNomEmpre(String NomEmpre) {
        this.NomEmpre = NomEmpre;
    }

    public String getRucEmpre() {
        return RucEmpre;
    }

    public void setRucEmpre(String RucEmpre) {
        this.RucEmpre = RucEmpre;
    }

    public String getDirEmpre() {
        return DirEmpre;
    }

    public void setDirEmpre(String DirEmpre) {
        this.DirEmpre = DirEmpre;
    }

    public String getTelEmpre() {
        return TelEmpre;
    }

    public void setTelEmpre(String TelEmpre) {
        this.TelEmpre = TelEmpre;
    }

    public String getCodUbi() {
        return CodUbi;
    }

    public void setCodUbi(String CodUbi) {
        this.CodUbi = CodUbi;
    }
    
    
}
